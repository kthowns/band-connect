package entity;

public class AplDetail {
    private Apl apl;
    private Post post;
    private Recruit recruit;
    private Band band;

    public Apl getApl() {
        return apl;
    }

    public void setApl(Apl apl) {
        this.apl = apl;
    }

    public Post getPost() {
        return post;
    }

    public void setPost(Post post) {
        this.post = post;
    }

    public Recruit getRecruit() {
        return recruit;
    }

    public void setRecruit(Recruit recruit) {
        this.recruit = recruit;
    }

    public Band getBand() {
        return band;
    }

    public void setBand(Band band) {
        this.band = band;
    }
}